let a = prompt("Nhập số a: ");
aS = Number(a);
document.write(Math.abs(aS) + "<br>"); // Math.abs() trả về giá trị tuyệt đối của một số
document.write(Math.ceil(aS) + "<br>"); // Math.ceil() trả về số nguyên lớn nhất mà không lớn hơn số đã cho
document.write(Math.floor(aS) + "<br>"); // Math.floor() trả về số nguyên nhỏ nhất mà không nhỏ hơn số đã cho
document.write(Math.round(aS)   ); // Math.round() trả về giá trị của số đã cho, làm tròn đến số nguyên gần nhất
