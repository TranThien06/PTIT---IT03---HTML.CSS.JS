let numBer = prompt("Nhập 1 số từ 0->9: ");
numBer = Number(numBer);
if (numBer === 1) {
    document.write("Số một <br>");
}
else if (numBer === 2) {
    document.write("Số hai <br>");
}
else if (numBer === 3) {
    document.write("Số ba <br>");
}
else if (numBer === 4) {
    document.write("Số bốn <br>");
}
else if (numBer === 5) {
    document.write("Số năm <br>");
}
else if (numBer === 6) {
    document.write("Số sáu <br>");
}
else if (numBer === 7) {
    document.write("Số bảy <br>");
}
else if (numBer === 8) {
    document.write("Số tám <br>");
}
else if (numBer === 9) {
    document.write("Số chín <br>");
}
else if (numBer === 0) {
    document.write("Số không <br>");
}
else {
    document.write("Số không hợp lệ <br>");
}